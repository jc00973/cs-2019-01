package com.github.jc00973.cs.aula10.domain;

/**
 * Classe dia que contém os valores correspondentes ao dias da semana.
 */
public final class Dia {

    /**
     * Valor correspondente ao dia de segunda-feira.
     */
    public static final int SEGUNDA = 0;

    /**
     * Valor correspondente ao dia de terca-feira.
     */
    public static final int TERCA = 1;

    /**
     * Valor correspondente ao dia de quarta-feira.
     */
    public static final int QUARTA = 2;

    /**
     * Valor correspondente ao dia de quinta-feira.
     */
    public static final int QUINTA = 3;

    /**
     * Valor correspondente ao dia de sexta-feira.
     */
    public static final int SEXTA = 4;

    /**
     * Valor correspondente ao dia de sábado.
     */
    public static final int SABADO = 5;

    /**
     * Valor correspondente ao dia de domingo.
     */
    public static final int DOMINGO = 6;

    /**
     * Construtor privado para evitar instanciação indevida.
     */
    private Dia() { };

}
